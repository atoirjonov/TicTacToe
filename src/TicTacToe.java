public class TicTacToe {
    public static void main(String[] args) {
        /*
        Create a 2D array named board in order
        to create 3 x 3 board
         */
        char [][] board = {
                {' ', ' ', ' '},
                {' ', ' ', ' '},
                {' ', ' ', ' '}
        };

        System.out.println(board[0][0] + " | " + board[0][1] + " | " + board[0][2]);
    }
}
